    RecyclerView recyclerView;
    ProgressBar pb;
    artistAdapter adapter;
    private List<artist> listData;
    @Override
    public View onCreateView(LayoutInflater inflater, ViewGroup container,
                             Bundle savedInstanceState) {
        View v = inflater.inflate(R.layout.fragment_blank, container, false);
        recyclerView=v.findViewById(R.id.recyclerView);
        pb=v.findViewById(R.id.pb);


        recyclerView.setHasFixedSize(true);
        recyclerView.setLayoutManager(new LinearLayoutManager(getContext()));
        listData=new ArrayList<>();

        final DatabaseReference nm= FirebaseDatabase.getInstance().getReference("events").child("gaming");
        nm.addListenerForSingleValueEvent(new ValueEventListener() {
            @Override
            public void onDataChange(DataSnapshot dataSnapshot) {
                if (dataSnapshot.exists()){
                    for (DataSnapshot npsnapshot : dataSnapshot.getChildren()){
                        artist l=npsnapshot.getValue(artist.class);
                        listData.add(l);
                    }
                    adapter= new  artistAdapter(listData,getActivity());
                    recyclerView.setAdapter(adapter);
                    pb.setVisibility(View.INVISIBLE);
                }
            }

            @Override
            public void onCancelled(DatabaseError databaseError) {

            }
        });

        return v;

    }




